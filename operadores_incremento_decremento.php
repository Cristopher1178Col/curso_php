<?php

//pre incremento ++ y variable
$numero=10;

echo ++$numero;

//post incremento variable y ++ (primero realiza la accion y luego incrementa)

$numero=10;

echo $numero++;
echo $numero;

// pre decremento -- y variable

$numero=10;

echo --$numero;


//post decremento variable y -- (primero realiza la accion y luego decrementa)

$numero=10;

echo $numero--;
echo $numero;
